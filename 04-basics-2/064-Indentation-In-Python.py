# 064 : Indentation In Python
# indentation is used to define block of code
# other languages use curly braces {} to define block of code

# Python: if condition: indented code block
# other languages: if (condition) { code block }
condition = True
if condition:
    print("This is True")
elif not condition:
    print("This is False")
else:
    print("This is none of the above")
print("new line after if block")

