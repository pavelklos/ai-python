# 021 : Numbers

# integer
int
print(2 + 4)
print(type(2 + 4))  # <class 'int'>
print(2 - 4)
print(2 * 4)
print(2 / 4)  # <class 'float'> for 0.5

# floating point number
float
print(2 / 4)
print(type(2 / 4))  # <class 'float'>
print(9.9 + 1.1)
print(type(9.9 + 1.1))  # <class 'float'> for 11.0

# double multiplication, division, modulo
print(2**3)  # 2^3 = 8           double multiply = power of
print(2 // 4)  # 2/4 = 0.5, 0      double slash
print(9 // 2)  # 9/2 = 4.5, 4
print(9 % 2)  # 9/2 = 4.5, 1      percent sign

# boolean
bool
print(type(2 + 4 == 8))  # <class 'bool'>
